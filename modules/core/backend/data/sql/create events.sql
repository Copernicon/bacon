SET NAMES utf8;
SET time_zone = '+00:00';
SET foreign_key_checks = 0;
SET sql_mode = 'NO_AUTO_VALUE_ON_ZERO';

CREATE EVENT IF NOT EXISTS expire_activations ON SCHEDULE EVERY 1 DAY STARTS '2005-04-02 21:37:00' ON COMPLETION PRESERVE DISABLE ON SLAVE DO DELETE FROM activations WHERE expiration < now();
CREATE EVENT IF NOT EXISTS expire_sessions ON SCHEDULE EVERY 1 DAY STARTS '2005-04-02 21:37:00' ON COMPLETION PRESERVE DISABLE ON SLAVE DO DELETE FROM sessions WHERE expiration < now();